        /// <summary>
        /// 標準ToUpperは意外と重いのである程度最適してみる
        /// サロゲートペアは非対応
        /// </summary>
        public static char ToUpperFast(char c)
        {
            if ('a' <= c && c <= 'z')
            {
                return (char)(c + ('A' - 'a'));
            }
            if (c < '\u007F')
            {
                return c;
            }

            // Upper処理が必要ないおおまかな範囲でトリミング
            if (('\u0587' <= c && c <= '\u10CF') ||
                ('\u1100' <= c && c <= '\u13F7') ||
                ('\u13FE' <= c && c <= '\u1C7F') ||
                ('\u1FF4' <= c && c <= '\u214D') ||
                ('\u2185' <= c && c <= '\u2C2F') ||
                ('\u2D2E' <= c && c <= '\uA640') ||
                ('\uA7F7' <= c && c <= '\uAB52') ||
                ('\uABC0' <= c && c <= '\uFF40'))
            {
                return c;
            }

            if (_upperCaseMap.TryGetValue(c, out char upperCase))
            {
                return upperCase;
            }
            else
            {
                return c;
            }
        }

